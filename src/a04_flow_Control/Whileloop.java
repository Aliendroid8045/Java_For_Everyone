package a04_flow_Control;

public class Whileloop {

	public static void main(String[] args) {
	// First we will define variable 	
		int a = 0;
		/*while loop checks the condition within the bracket and executes block 
		
		until the condition within the bracket is false.*/
		while ( a < 10){
			
			System.out.println("we are looping");
			
			a = a+1;
		}

	}

}
